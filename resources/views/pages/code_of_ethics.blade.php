                <h6 class="mt-5 mb-3 red fw-bold">Part 1: Relation between IBDL and The Accredited Training Centers</h6>
                <b>Integrity and Collaboration:</b> <br>
                <ul>
                    <li>
                        IBDL and the accredited training centers shall foster a relationship built on trust, integrity,
                        and mutual respect.
                    </li>
                    <li>
                        They shall collaborate transparently, ensuring effective communication and cooperation in
                        achieving shared goals.
                    </li>
                </ul>
                <b>Quality Assurance:</b> <br>
                <ul>
                    <li>
                        IBDL and the accredited training centers shall maintain the highest quality standards in
                        delivering education and training programs.
                    </li>
                    <li>
                        They shall work together to continuously assess and improve the effectiveness of the
                        programs, ensuring they meet or exceed industry standards.
                    </li>
                </ul>
                <b>Compliance with Standards and Guidelines:</b> <br>
                <ul>
                    <li>
                        IBDL and the accredited training centers shall adhere to the established standards, guidelines,
                        and policies set forth by IBDL.
                    </li>
                    <li>
                        They shall ensure compliance with all relevant laws and regulations governing their operations
                        and certifications.
                    </li>
                </ul>



                <h6 class="mt-5 mb-3 red fw-bold">Part 2: Relation between IBDL and the Certified Trainers</h6>
                <b>Professionalism and Competence:</b> <br>
                <ul>
                    <li>
                        IBDL and the certified trainers shall maintain professionalism and demonstrate competence.

                    </li>
                    <li>
                        They shall possess the qualifications, expertise, and knowledge to deliver educational content
                        aligned with IBDL's standards.

                    </li>
                </ul>
                <b> Ethical Conduct:</b> <br>
                <ul>
                    <li>

                        IBDL and the certified trainers shall conduct themselves ethically, treating all learners with
                        respect, fairness, and impartiality.

                    </li>
                    <li>
                        They shall refrain from engaging in any conduct that may undermine the learning process's
                        integrity or compromise the learner's trust.

                    </li>
                </ul>

                <h6 class="mt-5 mb-3 red fw-bold">Part 3: Relation between IBDL and the Learners</h6>
                <b>Learner-Centric Approach:</b> <br>
                <ul>
                    <li>
                        IBDL shall prioritize the needs and well-being of the learners, providing an inclusive and
                        supportive learning environment.
                    </li>
                    <li>
                        IBDL shall ensure the educational content and assessments are relevant, engaging, and aligned
                        with the learners' educational goals.
                    </li>
                </ul>
                <b> Transparent Information:</b> <br>
                <ul>
                    <li>
                        IBDL shall provide learners with accurate and comprehensive information about the certification
                        programs, including the learning objectives, assessment methods, and certification requirements.
                    </li>
                    <li>
                        IBDL shall address learner inquiries and concerns promptly and transparently, offering guidance
                        and support throughout their learning journey.
                    </li>
                </ul>


