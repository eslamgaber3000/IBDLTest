<p>&nbsp;IBDL Academic Accreditation is designed to help students choose business learning to support their professional
    business Administration careers. It recognizes universities that engage with the business Administration profession
    and who offer modules/courses/program frameworks to students which demonstrate alignment with the IBDL Body of
    Knowledge. Academic Accreditation assesses relevant degree-based modules, courses, and program frameworks.&nbsp;</p>
<p><strong>&nbsp;Benefits</strong>&nbsp;</p>
<ul>
    <li>&nbsp;A certificate, valid for three years&nbsp;</li>
    <li>&nbsp;An IBDL Accreditation logo to use on all relevant promotional material&nbsp;</li>
    <li>&nbsp;A listing and link on the IBDL platform&nbsp;</li>
    <li>&nbsp;A copy of the assessor’s report to use for further development of your program.&nbsp;</li>
    <li>&nbsp;The experience plus qualification route to IBDL Full Membership (route 1) will be available to any student
        who has successfully completed an accredited module or course, subject to meeting any eligibility date ranges
        that may be applied.&nbsp;</li>
</ul>
<p>Students on accredited academic courses are eligible for free IBDL Student Membership, offering access to IBDL
    platform. <br><strong>&nbsp;How to gain accreditation&nbsp;</strong></p>
<ul>
    <li><strong>&nbsp;Stage 1 – Initial discussion</strong><br> Once you register your interest in accreditation a
        business development manager will discuss your needs and your readiness to apply.&nbsp;</li>
    <li><strong>&nbsp;Stage 2 – Completing your application</strong>&nbsp;<br> You should answer the questions in the
        application form as fully as possible and provide relevant course materials.&nbsp;</li>
    <li><strong>&nbsp;Stage 3 – The assessment</strong><br> Your application will be sent to an assessor who will assess
        whether it meets the requirements of accreditation. An online ‘site visit’ will be arranged to clarify any
        outstanding issues.&nbsp;</li>
    <li><strong>Stage 4 – Assessment decision</strong><br> Our assessor will complete a report, making recommendations
        to IBDL. We will inform you promptly of the outcome. You will receive a copy of the report and advice on areas
        that require development in order to meet the standards of accreditation, if necessary.&nbsp;</li>
    <li><strong>&nbsp;Stage 5 - Re-assessment</strong><br> If your application is unsuccessful you will be provided with
        the assessor's report plus advice on areas requiring development. You may re-submit at any time. This will be
        treated as a new submission</li>
    <li><strong>&nbsp;Stage 6 – Annual renewal</strong><br> On the anniversary of your accreditation, you will be sent
        an annual report template and invoice and be asked to confirm that the standards recognized through your initial
        assessment have been maintained. On the third anniversary of your assessment, you will be invited to go through
        a full re-assessment.</li>
</ul>
